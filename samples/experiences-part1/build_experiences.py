def to_sec(h, m, s, ms): return int(h)*3600+int(m)*60+int(s)+int(ms)/1000.0
def fmt(t):
    if t < 0: t = 0
    ms = int(round(t*1000)); h, ms = divmod(ms, 3600000); m, ms = divmod(ms, 60000); s, ms = divmod(ms, 1000)
    return f"{h:02d}:{m:02d}:{s:02d},{ms:03d}"

raw = io.open(EN_SRC, encoding="utf-8-sig").read().replace("\r", "")
cues = []
for b in re.split(r"\n\s*\n", raw):
    m = TS.search(b)
    if not m: continue
    lines = b.split("\n"); ti = next(i for i, l in enumerate(lines) if "-->" in l)
    txt = re.sub(r"\s+", " ", " ".join(l.strip() for l in lines[ti+1:])).strip()
    cues.append((to_sec(*m.group(1,2,3,4)), to_sec(*m.group(5,6,7,8)), txt))

# interpolated per-word time stream
W = []
for s, e, t in cues:
    toks = [w for w in t.split(" ") if w]
    n = len(toks); span = max(e-s, 0.001)
    for k, w in enumerate(toks):
        W.append((re.sub(r"[^a-z0-9]", "", w.lower()), s + (span*k/n if n > 1 else 0)))
LAST_END = cues[-1][1]

def norm(s): return [re.sub(r"[^a-z0-9]", "", w.lower()) for w in s.split() if re.sub(r"[^a-z0-9]", "", w.lower())]

